// app/api/auth/face-login/route.ts - –ü–æ–ª–Ω–æ—Ü–µ–Ω–Ω—ã–π –≤—Ö–æ–¥ —á–µ—Ä–µ–∑ Face ID
import { NextRequest, NextResponse } from 'next/server';
import { createSession } from '@/lib/simple-auth';
import { faceIdStorage } from '@/lib/face-id-storage';
import jwt from 'jsonwebtoken';
import { UserRole } from '@/lib/permissions';

interface FaceLoginRequest {
  descriptor: number[];
  confidence: number;
  faceFingerprint?: string;
  metadata?: {
    source?: string;
    timestamp?: number;
  };
}

// –ü–æ—Ä–æ–≥ —Å—Ö–æ–∂–µ—Å—Ç–∏ –¥–ª—è Face ID (0.6 = 60% —Å—Ö–æ–∂–µ—Å—Ç–∏)
const SIMILARITY_THRESHOLD = parseFloat(process.env.FACE_ID_THRESHOLD || '0.6');

// –ú–∏–Ω–∏–º–∞–ª—å–Ω–∞—è —É–≤–µ—Ä–µ–Ω–Ω–æ—Å—Ç—å –¥–ª—è –≤—Ö–æ–¥–∞
const MIN_CONFIDENCE = 60;

export async function POST(request: NextRequest) {
  console.log('üë§ Face Login: –Ω–∞—á–∞–ª–æ –≤—Ö–æ–¥–∞ —á–µ—Ä–µ–∑ Face ID');
  const startTime = Date.now();

  try {
    const body: FaceLoginRequest = await request.json();
    const { descriptor, confidence, faceFingerprint, metadata } = body;

    console.log('üë§ Face Login: –ø–æ–ª—É—á–µ–Ω—ã –¥–∞–Ω–Ω—ã–µ:', {
      hasDescriptor: !!descriptor,
      descriptorLength: descriptor?.length,
      confidence,
      hasFaceFingerprint: !!faceFingerprint,
      metadata
    });

    // ‚úÖ –ë–∞–∑–æ–≤–∞—è –≤–∞–ª–∏–¥–∞—Ü–∏—è
    if (!descriptor || descriptor.length < 128) {
      return NextResponse.json({
        success: false,
        message: '–ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –¥–∞–Ω–Ω—ã—Ö –ª–∏—Ü–∞ –¥–ª—è –≤—Ö–æ–¥–∞'
      }, { status: 400 });
    }

    if (confidence < MIN_CONFIDENCE) {
      return NextResponse.json({
        success: false,
        message: `–ö–∞—á–µ—Å—Ç–≤–æ —Ä–∞—Å–ø–æ–∑–Ω–∞–≤–∞–Ω–∏—è —Å–ª–∏—à–∫–æ–º –Ω–∏–∑–∫–æ–µ (${confidence}%). –ú–∏–Ω–∏–º—É–º ${MIN_CONFIDENCE}%`
      }, { status: 400 });
    }

    // ‚úÖ –ü–æ–∏—Å–∫ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–µ–≥–æ Face ID –ø—Ä–æ—Ñ–∏–ª—è –ø–æ –¥–µ—Å–∫—Ä–∏–ø—Ç–æ—Ä—É
    console.log('üîç –ü–æ–∏—Å–∫ Face ID –ø—Ä–æ—Ñ–∏–ª—è –ø–æ –¥–µ—Å–∫—Ä–∏–ø—Ç–æ—Ä—É...');
    
    const match = await faceIdStorage.findByDescriptor(descriptor, SIMILARITY_THRESHOLD);
    
    if (!match) {
      console.log('‚ùå Face ID –ø—Ä–æ—Ñ–∏–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω (—Å—Ö–æ–∂–µ—Å—Ç—å –Ω–∏–∂–µ –ø–æ—Ä–æ–≥–∞)');
      
      // –õ–æ–≥–∏—Ä—É–µ–º –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏
      const stats = await faceIdStorage.getStats();
      console.log('üìä –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ Face ID:', stats);
      
      return NextResponse.json({
        success: false,
        message: 'Face ID –Ω–µ —Ä–∞—Å–ø–æ–∑–Ω–∞–Ω. –£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ –≤—ã –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–ª–∏ Face ID –≤ —Å–∏—Å—Ç–µ–º–µ.',
        debug: process.env.NODE_ENV === 'development' ? {
          threshold: SIMILARITY_THRESHOLD,
          totalProfiles: stats.totalProfiles,
          activeProfiles: stats.activeProfiles
        } : undefined
      }, { status: 404 });
    }

    const { profile, similarity } = match;
    
    console.log('‚úÖ Face ID –ø—Ä–æ—Ñ–∏–ª—å –Ω–∞–π–¥–µ–Ω:', {
      profileId: profile.id,
      userId: profile.userId,
      userName: profile.userName,
      similarity: `${(similarity * 100).toFixed(1)}%`,
      confidence: `${confidence}%`
    });

    // ‚úÖ –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞ –∫–∞—á–µ—Å—Ç–≤–∞ —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è
    const combinedScore = (similarity + confidence / 100) / 2;
    if (combinedScore < 0.65) { // 65% –∫–æ–º–±–∏–Ω–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –ø–æ—Ä–æ–≥
      console.log('‚ö†Ô∏è –ù–∏–∑–∫–∏–π –∫–æ–º–±–∏–Ω–∏—Ä–æ–≤–∞–Ω–Ω—ã–π score:', combinedScore);
      return NextResponse.json({
        success: false,
        message: '–ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–∞—è —É–≤–µ—Ä–µ–Ω–Ω–æ—Å—Ç—å –≤ —Ä–∞—Å–ø–æ–∑–Ω–∞–≤–∞–Ω–∏–∏. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ —É–ª—É—á—à–∏—Ç—å –æ—Å–≤–µ—â–µ–Ω–∏–µ.'
      }, { status: 400 });
    }

    // ‚úÖ –û–±–Ω–æ–≤–ª—è–µ–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è
    await faceIdStorage.updateUsageStats(profile.id);

    // ‚úÖ –°–æ–∑–¥–∞–µ–º —Å–µ—Å—Å–∏—é –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    console.log('üîê –°–æ–∑–¥–∞–µ–º —Å–µ—Å—Å–∏—é –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è:', profile.userId);
    
    const sessionToken = await createSession({
      id: profile.userId,
      email: profile.userEmail,
      role: profile.userRole as UserRole,
      name: profile.userName,
      createdAt: new Date(),
      updatedAt: new Date()
    });

    // ‚úÖ –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—ã–π Face ID —Ç–æ–∫–µ–Ω
    const faceIdToken = faceIdStorage.createFaceIdToken(profile);

    // ‚úÖ –û–ø—Ä–µ–¥–µ–ª—è–µ–º URL –¥–∞—à–±–æ—Ä–¥–∞ –ø–æ —Ä–æ–ª–∏
    const dashboardUrls: Record<string, string> = {
      'admin': '/admin',
      'super-admin': '/admin',
      'manager': '/manager-dashboard',
      'trainer': '/trainer-dashboard',
      'client': '/member-dashboard',
      'member': '/member-dashboard',
    };

    const dashboardUrl = dashboardUrls[profile.userRole] || '/member-dashboard';

    // ‚úÖ –°–æ–∑–¥–∞–µ–º response
    const response = NextResponse.json({
      success: true,
      message: `–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å, ${profile.userName}!`,
      user: {
        id: profile.userId,
        name: profile.userName,
        email: profile.userEmail,
        role: profile.userRole as UserRole
      },
      authMethod: "face_recognition",
      dashboardUrl,
      metrics: {
        similarity: Math.round(similarity * 100),
        confidence: Math.round(confidence),
        combinedScore: Math.round(combinedScore * 100),
        processingTime: Date.now() - startTime
      }
    });

    // ‚úÖ –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º cookies –¥–ª—è –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏
    const cookieOptions = {
      httpOnly: true,
      secure: process.env.NODE_ENV === 'production',
      sameSite: 'lax' as const,
      maxAge: 24 * 60 * 60, // 24 —á–∞—Å–∞
      path: '/'
    };

    // JWT —Ç–æ–∫–µ–Ω –¥–ª—è –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏
    response.cookies.set('auth_token', sessionToken, cookieOptions);
    response.cookies.set('session_id', sessionToken, cookieOptions);
    
    // –†–æ–ª—å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è (–Ω–µ httpOnly –¥–ª—è –¥–æ—Å—Ç—É–ø–∞ –∏–∑ JS)
    response.cookies.set('user_role', profile.userRole, {
      ...cookieOptions,
      httpOnly: false
    });

    // –û–±–Ω–æ–≤–ª—è–µ–º Face ID —Ç–æ–∫–µ–Ω
    response.cookies.set('face_id_registered', faceIdToken, {
      ...cookieOptions,
      maxAge: 30 * 24 * 60 * 60 // 30 –¥–Ω–µ–π
    });

    // ID –ø—Ä–æ—Ñ–∏–ª—è
    response.cookies.set('face_id_profile', profile.id, {
      ...cookieOptions,
      httpOnly: false,
      maxAge: 30 * 24 * 60 * 60
    });

    console.log('‚úÖ Face ID –≤—Ö–æ–¥ —É—Å–ø–µ—à–µ–Ω –¥–ª—è:', profile.userName);
    console.log('‚è±Ô∏è –í—Ä–µ–º—è –æ–±—Ä–∞–±–æ—Ç–∫–∏:', Date.now() - startTime, 'ms');
    
    return response;

  } catch (error) {
    console.error('‚ùå Face Login: –æ—à–∏–±–∫–∞:', error);
    
    return NextResponse.json({
      success: false,
      message: '–û—à–∏–±–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞ –ø—Ä–∏ –≤—Ö–æ–¥–µ —á–µ—Ä–µ–∑ Face ID',
      error: process.env.NODE_ENV === 'development' ? 
        (error instanceof Error ? error.message : String(error)) : undefined
    }, { status: 500 });
  }
}

// ‚úÖ GET –º–µ—Ç–æ–¥ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç–∏ Face ID –≤—Ö–æ–¥–∞
export async function GET(request: NextRequest) {
  try {
    const stats = await faceIdStorage.getStats();
    
    return NextResponse.json({
      enabled: true,
      message: 'Face ID login endpoint',
      requirements: {
        descriptorLength: 128,
        minConfidence: MIN_CONFIDENCE,
        similarityThreshold: Math.round(SIMILARITY_THRESHOLD * 100)
      },
      stats: {
        totalProfiles: stats.totalProfiles,
        activeProfiles: stats.activeProfiles,
        totalUsers: stats.totalUsers
      },
      timestamp: new Date().toISOString()
    });
  } catch (error) {
    console.error('‚ùå Face Login GET: –æ—à–∏–±–∫–∞:', error);
    
    return NextResponse.json({
      enabled: false,
      message: 'Face ID –≤—Ä–µ–º–µ–Ω–Ω–æ –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω',
      error: process.env.NODE_ENV === 'development' ? 
        (error instanceof Error ? error.message : String(error)) : undefined
    }, { status: 500 });
  }
}